//woriking of increment and decrement operator
#include<stdio.h>
int main()
{
    // assign the value 
    int a = 100 ;
    int b = 200 ;
    float c = 500.50 ;
    float d = 250.50;
    // print the increment and decrement operator value 
    printf("++a = %d\n" , ++a);
    printf("b++ = %d\n" , b++);
    printf("--c = %f\n" , --c);
    printf("d-- = %f\n" , c--);
    return 0;
    
}